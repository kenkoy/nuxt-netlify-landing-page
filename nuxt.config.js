const glob = require('glob')
const path = require('path')

var getDynamicRoutes = function() {
  return [].concat(
    glob
      .sync('*.json', { cwd: 'assets/content/blog' })
      .map((filepath) => `/posts/${path.basename(filepath, '.json')}`),
  )
}

